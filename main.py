
def checkgrade(point):
    if point >= 90:
        print('เกรด: A')
    elif point >= 85:
        print('เกรด: B+')
    elif point >= 80:
        print('เกรด: B')
    elif point >= 75:
        print('เกรด: C+')
    elif point >= 70:
        print('เกรด: C')
    elif point >= 65:
        print('เกรด: D+')
    elif point >= 60:
        print('เกรด: D')
    else:
        print('เกรด: F')
checkgrade(85)

